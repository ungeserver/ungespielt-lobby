package net.ungespielt.lobby.shared;

/**
 * The visibility states of the other players.
 *
 * @author Felix Klauke <info@felix-klauke.de>
 */
public enum PlayerVisibilityState {

    /**
     * All players are visible.
     */
    ALL,

    /**
     * Only cool players are visible.
     */
    TEAM,

    /**
     * Nobody is visible.
     */
    NONE
}
